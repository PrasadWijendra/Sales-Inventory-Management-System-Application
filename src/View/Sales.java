    private void btn_sellMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btn_sellMouseClicked
        try{ Double Uprice,totprice,balance;
             String id,name,category;
             int Qty,cash;
        
        id=txt_id.getText();
        name=txt_name.getText();
        Uprice=Double.parseDouble(txt_uprice.getText());
        Qty=Integer.parseInt(txt_qty.getText());
        totprice=Double.parseDouble(txt_tot.getText());
        cash=Integer.parseInt(txt_cash.getText());
        balance=Double.parseDouble(txt_balance.getText());
        
         if (id.isEmpty()) 
       {
        JOptionPane.showMessageDialog(null, "Please enter ID");
       } 
       
       else if ( Qty <= 0) 
        {
        JOptionPane.showMessageDialog(null, "Please enter a valid quantity");
        } 
      
       else if ( cash <= 0) 
        {
        JOptionPane.showMessageDialog(null, "Please enter a valid cash");
        } 
       
       else {
        
       
        
        CitemSales ci=new CitemSales();
        ci.CsaleItems(id, name, totprice, Qty, totprice, cash, balance);
        
         txt_id.setText("");
        txt_name.setText("");
        txt_uprice.setText("");
        txt_qty.setText("");
        txt_tot.setText("");
        txt_cash.setText("");
        txt_balance.setText("");
       }
    }
    catch (NumberFormatException ex) 
    {
    JOptionPane.showMessageDialog(null, "Please enter quantity & cash Numbers only!");
    } 
    catch (Exception ex) 
    {
    JOptionPane.showMessageDialog(null, "Please try again");
    ex.printStackTrace(); // 
    }
        
    }//GEN-LAST:event_btn_sellMouseClicked

