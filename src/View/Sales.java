    private void txt_idKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_idKeyReleased
       try{  
        String Id;
            Id=txt_id.getText();

            CitemSearch is=new CitemSearch();
            ResultSet rst=is.CsearchItem(Id);
            if(rst.next())
            {
            
                txt_name.setText(rst.getString(1));
                 txt_uprice.setText(String.valueOf(rst.getDouble(2)));
            }
            
                    
        
       }
       catch(SQLException e)
       {
           System.err.println(e.getMessage());
       }
    }//GEN-LAST:event_txt_idKeyReleased

    private void txt_qtyKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_qtyKeyReleased
       Double uprice=Double.parseDouble(txt_uprice.getText());
       int quantity=Integer.parseInt(txt_qty.getText());
       Double totp=uprice*quantity;
       txt_tot.setText(totp+"");
    }//GEN-LAST:event_txt_qtyKeyReleased

    private void txt_cashKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_cashKeyReleased
        int cash=Integer.parseInt(txt_cash.getText());
        Double totp=Double.parseDouble(txt_tot.getText());
        Double balance=cash-totp;
        txt_balance.setText(balance+"");
    }//GEN-LAST:event_txt_cashKeyReleased

